int sumanteros(int nas1, int nas2)
{
    int resultado;
    resultado=nas1+nas2;
    return resultado;
}

int restanteros(int nar1, int nar2)
{
    int resultado;
    resultado=nar1-nar2;
    return resultado;
}

int multiteros(int nam1, int nam2)
{
    int resultado;
    resultado=nam1*nam2;
    return resultado;
}

int diviteros(float nad1, float nad2)
{
    float resultado;
    resultado=(nad1/nad2);
    return resultado;
}
